<h3>Routify</h3>

<p>Routify is the router and along with Svelte it powers these templates.</p>
<p>Routify outputs to <code>.routify</code> by default.</p>
<p>
  Configuration can be done in <code>package.json</code> and other places. For
  more info on configuration, see
  <a href="https://routify.dev/docs/config/build">Routify - configuration</a>
</p>
<p>
  For general usage, see <a
    href="https://routify.dev/guide/introduction/getting-started"
    >Getting started</a
  >.
</p>
