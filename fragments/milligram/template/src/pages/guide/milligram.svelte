<h3>Milligram.css</h3>

<p>Milligram is a lightweight css framework.</p>
<p>
    It's the default CSS framework for our examples and templates since it
    doesn't pollute the HTML with proprietary classes or contrived structures.
</p>
<p>To remove it, simply delete it from <code>__app.html</code></p>
<p>For more info: <a href="https://milligram.io/">milligram</a></p>
